#include "led.h"

#include <xc.h>

#include "mcc.h"

//-----------------------------------------------------------------------------
void FlashLed100ms()
{
    LED1_SetHigh();
    __delay_ms(100);
    LED1_SetLow();
}
//-----------------------------------------------------------------------------

//-----------------------------------------------------------------------------
void FlashLedCallback(void* param)
{
    uint8_t time = 1;
    if(param)
    {
        time = *((uint8_t*)param);
    }
    uint8_t i;
    for(i = 0;i<time;++i)
    {
        FlashLed100ms();
    }
}
//-----------------------------------------------------------------------------
